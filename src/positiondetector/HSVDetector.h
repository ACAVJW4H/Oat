#include "PositionDetector.h"

namespace oat {

class Position2D;

// Constants
static constexpr double PI {3.14159265358979323846};

/**
 * A color-based object position detector
 */
class HSVDetector : public PositionDetector {
public:

    /**
     * A color-based object position detector with default parameters.
     * @param frame_source_address Frame SOURCE node address
     * @param position_sink_address Position SINK node address
     */
    HSVDetector(const std::string &frame_source_address,
                const std::string &position_sink_address);

    /**
     * Perform color-based object position detection.
     * @param frame frame to look for object in.
     * @return  detected object position.
     */
    oat::Position2D detectPosition(cv::Mat &frame) override;

    void configure(const std::string &config_file, const std::string &config_key);

private:

    // Sizes of the erode and dilate blocks
    int erode_px_ {0}, dilate_px_ {10};
    bool erode_on_ {false}, dilate_on_ {false};

    cv::Mat threshold_frame_, erode_element_, dilate_element_;

    // HSV threshold values
    int h_min_ {0}, h_max_ {256};
    int s_min_ {0}, s_max_ {256};
    int v_min_ {0}, v_max_ {256};

    // Detect object area
    double object_area_ {0.0};
    double min_object_area_ {0.0};
    double max_object_area_ {std::numeric_limits<double>::max()};

    // The detected object position
    oat::Position2D object_position_;

    // Parameter tuning GUI functions and properties
    bool tuning_windows_created_ {false};
    const std::string tuning_image_title_;
    cv::Mat tune_image_;
    virtual void tune(cv::Mat &frame);
    virtual void createTuningWindows(void);
    static void minAreaSliderChangedCallback(int, void *);
    static void maxAreaSliderChangedCallback(int, void *);
    static void erodeSliderChangedCallback(int, void *);
    static void dilateSliderChangedCallback(int, void *);

    // Accessors (for tuning GUI, not accessible to users...)
    void set_erode_size(int erode_px);
    void set_dilate_size(int dilate_px);
    void set_min_object_area(double value) { min_object_area_ = value; }
    void set_max_object_area(double value) { max_object_area_ = value; }
};

// Helper functions

/**
 * Given a binary frame, find all contours and return a position corresponding
 * to the centroid of the largest one.
 * @param frame_in Frame to look for positions in.
 * @param position Position output
 * @param min_area Minimum contour area to be considered candidate for position
 * @param max_area Maximum contour area to be considered candidate for position
 * @return Position corresponding the centroid of the largest contour in the frame.
 */
void siftContours(cv::Mat &frame, Position2D &position, double min_area, double max_area);

}       /* namespace oat */
#endif	/* OAT_HSVDETECTOR_H */

